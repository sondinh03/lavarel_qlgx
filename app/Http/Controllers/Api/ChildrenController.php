     */
    public function index()
    {
        if(!empty($_GET['q']) AND !empty($_GET['giaoxu'])){
            $giaoxu = $_GET['giaoxu'];
            $search = $_GET['q'];
            
            if ($search) {
                $results = Children::where('name', 'LIKE', '%'.$search.'%')->where('pid', '=', $giaoxu)->where('status', '=', 1)->paginate(10);
            } else {
                $results = Children::paginate(10);
            }
            return $results;
        }
    }

